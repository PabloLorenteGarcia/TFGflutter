  // Colores primarios
  static const Color primary = Color(0xFF4CAF50);
  static const Color primaryLight = Color(0xFFC8E6C9);
  static const Color primaryDark = Color(0xFF388E3C);
  
  // Colores secundarios
  static const Color secondary = Color(0xFF8D6E63);
  static const Color secondaryLight = Color(0xFFD7CCC8);
  static const Color secondaryDark = Color(0xFF5D4037);
  
  // Color de acento
  static const Color accent = Color(0xFFFFC107);
  static const Color accentLight = Color(0xFFFFE082);
  
  // Colores de fondo
  static const Color background = Color(0xFFFAFAFA);
  static const Color surface = Color(0xFFFFFFFF);
  static const Color surfaceVariant = Color(0xFFF5F5F5);
  
  // Colores de texto
  static const Color textPrimary = Color(0xFF212121);
  static const Color textSecondary = Color(0xFF757575);
  static const Color textHint = Color(0xFFBDBDBD);
  
  // Colores de estado
  static const Color error = Color(0xFFF44336);
  static const Color success = Color(0xFF4CAF50);
  static const Color warning = Color(0xFFFF9800);
  static const Color info = Color(0xFF2196F3);
  
  // Colores para categorías de plantas
